	AwardPoint = function(self, sPlayer, iTeam, iPlayerTeam, iPoint, iDistance, idPlayer)
		
		------------
		if (idPlayer and idPlayer ~= NULL_ENTITY) then
			self.aMVPs[idPlayer] = self.aMVPs[idPlayer] or { 0, sPlayer }
			self.aMVPs[idPlayer][1] = (self.aMVPs[idPlayer][1] or 0) + 1
			self.aMVPs[idPlayer][2] = sPlayer
		end
		
		------------
		self.iTeamPoints[iTeam] = self.iTeamPoints[iTeam] + iPoint
		if (self.iTeamPoints[iTeam] >= self.cfg.GoalPoints) then
			return self:OnGameWon(sPlayer, iTeam, iPlayerTeam, iPoint, iDistance)
		end
	
		------------
		local sLeader = ""
		local iOtherTeam = (iTeam == 2 and 1 or 2)
		local iOtherPoints = self.iTeamPoints[iOtherTeam]
		local iPoints = self.iTeamPoints[iTeam]
		
		if (iPoints > iOtherPoints) then
			if (iPoints -1 == iOtherPoints) then
				sLeader = "(They are leading by one Point!)"
			else
				sLeader = "(They are Leading by " .. (iPoints - iOtherPoints) .. " Points!)"
			end
		elseif (iPoints < iOtherPoints) then
			if (iPoints + 1 == iOtherPoints) then
				sLeader = "(They are only one Point behind!)"
			else
				sLeader = "(They are " .. iOtherPoints - iPoints .. " Points Behind)"
			end
		else
			sLeader = "(Both Teams now Scored " .. iPoints .. " Points)"
		end
		
		------------
		SendMsg(CHAT_FBALL, ALL, "(%0.2fm) %s Scored %d Point%s for Team %s %s", iDistance, sPlayer, iPoint, (iPoints > 1 and "s" or ""), (iTeam == 1 and "NK" or "US"), (sLeader or ""))
		Debug("Cha FMALL",CHAT_FBALL)
		
		------------
		local vTeamFlags = self.Stadium.TeamFlags[(iTeam == 1 and 2 or 1)]
		for i, vFlag in pairs(vTeamFlags) do
			SpawnEffect(ePE_Firework, vFlag, g_Vectors.up, 1)
		end

	end,
	
