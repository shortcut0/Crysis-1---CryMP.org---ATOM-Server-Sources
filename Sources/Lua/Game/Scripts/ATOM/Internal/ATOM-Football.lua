	GlobalizePos = function(self, vBBoxes, vBase)
	
		local aNewBBoxes = {}
		for i, aBBox in pairs(vBBoxes) do
			if (not isVec(aBBox)) then
			
				if (isVec(aBBox[1])) then
					aNewBBoxes[i] = self:GlobalizePos(aBBox, vBase)
				else
					aNewBBoxes[i] = { x = {}, y = {}, z = {} }
					aNewBBoxes[i].x[1] = aBBox.x[1] + vBase.x
					aNewBBoxes[i].x[2] = aBBox.x[2] + vBase.x
					aNewBBoxes[i].y[1] = aBBox.y[1] + vBase.y
					aNewBBoxes[i].y[2] = aBBox.y[2] + vBase.y
					aNewBBoxes[i].z[1] = aBBox.z[1] + vBase.z
					aNewBBoxes[i].z[2] = aBBox.z[2] + vBase.z
				end
			else
				aNewBBoxes[i] = {
					x = aBBox.x + vBase.x,
					y = aBBox.y + vBase.y,
					z = aBBox.z + vBase.z,
				}
			end
		end
		return aNewBBoxes
	
