	AssignTeam = function(self, hPlayer, bIA)
		local iNK = 0
		local iUS = 0
		local iTeam = 0
		local sTeam = ""
		
		for i, hTarget in pairs(GetPlayers()) do
			if (hTarget.InStadium) then
			
				iTeam = g_game:GetTeam(hTarget.id)
				if (iTeam == TEAM_NK) then
					iNK = iNK + 1
				elseif (iTeam == TEAM_US) then
					iUS = iUS + 1
				end
			end
		end
		
		if (iNK > iUS) then
			if (not bIA) then g_utils:SwitchTeam(hPlayer.id, TEAM_US) end
			SendMsg(CENTER, hPlayer, "For Optimal Balance, you have been moved to the US Team!")
			sTeam = "us"
		elseif (iUS > iNK) then
			if (not bIA) then g_utils:SwitchTeam(hPlayer.id, TEAM_NK) end
			SendMsg(CENTER, hPlayer, "For Optimal Balance, you have been moved to the NK Team!")
			sTeam = "nk"
		else
			sTeam = GetRandom({"nk","us"})
		end
		
		if (g_game:GetTeam(hPlayer.id) == 0 and (not bIA)) then
			g_utils:SwitchTeam(hPlayer.id, TEAM_NK)
			g_utils:RevivePlayer(hPlayer, hPlayer)
		end
		
		return sTeam
	end,
	
