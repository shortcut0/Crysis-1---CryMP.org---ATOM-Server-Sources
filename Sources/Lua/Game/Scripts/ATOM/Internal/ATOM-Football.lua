	IsInGate = function(self, hBall, vBall)
	
		------------
		local aGates = self.Stadium.Gates
		local aGateNK = aGates[1]
		local aGateUS = aGates[2]
		
		------------
		local bInNK = self:WithinBBox(aGateNK, vBall)
		local bInUS = self:WithinBBox(aGateUS, vBall)
		
		------------
		local iDist = 0
		local sPlayer = (hBall.LastShooterName or "<The Force>")
		local idPlayer = (hBall.LastShooterId or NULL_ENTITY)
		local iTeam = 0
		if (hBall.LastShooterEnt and g_gameRules.class == "InstantAction") then
			iTeam = (hBall.LastShooterEnt.FootBallTeam or 0)
		elseif (hBall.LastShooterTeam) then
			iTeam = hBall.LastShooterTeam
		end
		
		if (hBall.LastHitPos) then
			iDist = GetDistance(hBall.LastHitPos, vBall)
		end
		
		--Debug("shooter team", iTeam)
		
		------------
		if (bInNK and iTeam ~= 1) then
			return true, self:AwardPoint(sPlayer, 2, iTeam, 1, iDist, idPlayer), 1	
		elseif (bInUS and iTeam ~= 2) then
			return true, self:AwardPoint(sPlayer, 1, iTeam, 1, iDist, idPlayer), 2
		end
		
		------------
		return false
	
	end,
	
